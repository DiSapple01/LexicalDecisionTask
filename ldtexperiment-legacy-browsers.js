function byebyeRoutineBegin(snapshot) {
  return async function () {
    TrialHandler.fromSnapshot(snapshot); // ensure that .thisN vals are up to date
    
    //--- Prepare to start Routine 'byebye' ---
    t = 0;
    frameN = -1;
    continueRoutine = true; // until we're told otherwise
    byebyeClock.reset();
    routineTimer.reset();
    byebyeMaxDurationReached = false;
    // update component parameters for each repeat
    /*
    * This is the final version of the code for saving data.
    * It sends all experiment data as a single CSV block to a Google Form.
    */
    
    // --- 1. Set up your Google Form details ---
    const formID = '1FAIpQLSf1COv6SZVcZwzqmE2saYXxKiImrjOdcPIEbkoNHdVF7XTc_Q';         // <-- PASTE YOUR FORM ID HERE
    const entryID = 'entry.1203359234';  // <-- PASTE YOUR ENTRY ID HERE (it must start with 'entry.')
    
    // --- 2. Define the data ---
    const data = psychoJS.experiment._trialsData;
    
    // --- 3. Function to convert data to a single CSV string ---
    function toCsv(data) {
        const headers = Object.keys(data[0]);
        const csvRows = [];
        csvRows.push(headers.join(','));
    
        for (const row of data) {
            const values = headers.map(header => {
                const escaped = ('' + row[header]).replace(/"/g, '\\"');
                return `"${escaped}"`;
            });
            csvRows.push(values.join(','));
        }
        // Join all rows with a newline character to make a single block of text
        return csvRows.join('\n');
    }
    const csvData = toCsv(data);
    
    // --- 4. Send the data to the Google Form ---
    // We need to encode the data so it can be sent in a URL
    const encodedData = encodeURIComponent(csvData);
    const url = `https://docs.google.com/forms/d/e/${formID}/formResponse?${entryID}=${encodedData}`;
    
    // This sends the data silently in the background
    fetch(url, {
        method: 'POST',
        mode: 'no-cors', // This is important to avoid security errors
    }).then(() => {
        // This part runs after the data has been sent.
        // It tells PsychoJS to quit, allowing the "Thank You" screen to appear.
        psychoJS.quit();
    });
    
    psychoJS.experiment.addData('byebye.started', globalClock.getTime());
    byebyeMaxDuration = null
    // keep track of which components have finished
    byebyeComponents = [];
    byebyeComponents.push(text_3);
    
    byebyeComponents.forEach( function(thisComponent) {
      if ('status' in thisComponent)
        thisComponent.status = PsychoJS.Status.NOT_STARTED;
       });
    return Scheduler.Event.NEXT;
  }
}


